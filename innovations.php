                            <h2>Eco-friendly stoves for families in villages</h2>
                            <h4>Background</h4><br>
                            <p>It is now understood that emissions from traditional cook stoves pose a threat bigger than previously thought. The newest health data from WHO estimates that 4.3 million people die annually from household air pollution caused by cooking with biomass and coal. It is the greatest health risk in the world after high blood pressure, tobacco and alcohol. More people are dying from the incremental, ongoing inhalation of smoke from fires they ignite in their homes than from malaria, tuberculosis and HIV/AIDS combined.
                                <br><br>
                            For over two decades, the Government of India, many State Government departments, Science & Technology Institutes, many public &private enterprises, multi-national energy majors and several NGOs in India have been working independently and also collaborating on various ‘Clean Cook Stove / Smokeless stove Initiatives’, trying to create a positive impact on environmental and social issues.
                            <br><br>
                            Smokeless household stoves are more fuel efficient than the conventional stoves with fuel efficiency up to 30%. Annually 500 kg of fuel wood can be saved from each stove. Smokeless household stoves typically consist of 2 pan with 1 fuel feeding port. This requires less space of 2.5 ft x 1.5 feet.
                            <br><br>
                            Construction and maintenance is also very easy and any agro waste can be used as fuel.

                            </p>
                            <h4>Our Approach</h4><br>
                            <p>MMK Foundation has taken an organic approach to the ‘Clean Cook Stove initiatives’ in the rural segment, taking into consideration the social & environmental impact versus business opportunities. MMK Foundation has been engaged in training rural women to build fuel efficient smokeless stoves for themselves and in their neighborhoods and to create smoke free kitchens in rural Tamilnadu. It has been popularizing the Vahni (Travels with wind) stove as a fuel-efficient smokeless wood burning stove.
                            <br><br>
                            The Vahni stove is built using a mould (made of MS) and uses locally available materials like mud, bricks and an AC chimney pipe.The cost of the stove including grate and chimney is less than Rs 1000 (less than USD 15) and this includes the amount paid to the woman stove builder for stove construction.
                            <br><br>
                            Recently, MMK Foundation has developed a larger version of the Vahni stove, ideal for use in mid day meal cooking and institutional cooking.
                            <br><br>
                            MMK Foundation has developed a comprehensive Awareness cum training program on Vahni stove construction. We have also come out with a short video version of the same. We are happy to partner with other grass root agencies and communities to adopt these stoves for different regions and to ensure wider dissemination of these stoves to different communities across India. However we suggest that such dissemination / requests are routed through us to ensure that the stoves meet the local needs appropriately, in terms of technology. MMK Foundation can also facilitate construction of stoves through women trained by us already.
